<?php namespace NukaCode\Core\Models\User\Preference;

use NukaCode\Core\Models\Relationships\User\Preference\User as UserRelationshipsTrait;

class User extends \BaseModel {
    /********************************************************************
     * Traits
     *******************************************************************/
    use UserRelationshipsTrait;

    /********************************************************************
     * Declarations
     *******************************************************************/

    protected        $table    = 'preferences_users';

    protected static $observer = 'NukaCode\Core\Models\Observers\User\Preference\UserObserver';

    /********************************************************************
     * Validation rules
     *******************************************************************/

    /********************************************************************
     * Scopes
     *******************************************************************/

    /********************************************************************
     * Model Events
     *******************************************************************/

    /********************************************************************
     * Getter and Setter methods
     *******************************************************************/

    /********************************************************************
     * Extra Methods
     *******************************************************************/
    public function validateValue()
    {
        return (preg_match('/' . $this->preference->value . '/', $this->value) == 1 ? true : false);
    }
}
